	public int getMin(int[] ar)
	{
        int n = ar.length;
        int cnt = 0, add;
        while (!IsAll0(ar))
        {
            for (int i = 0; i < n; i++)
            {
                add = ar[i] % 2;
                ar[i] /= 2;
                cnt += add;
            }
            cnt++;  //double
        }
        if (cnt > 0) cnt--; //add and double together
        return cnt;
	}

    private boolean IsAll0(int[] a)
    {
        for (int i = 0; i < a.length; i++)
            if (a[i] > 0) return false;
        return true;
    }
	
