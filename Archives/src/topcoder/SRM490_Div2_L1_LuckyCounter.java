	public int countLuckyMoments(String[] moments)
	{
        char h1, h2, m1, m2;
        int cnt = 0;
        for (String m : moments)
        {
        	char[] s = m.toCharArray();
            h1 = s[0];
            h2 = s[1];
            m1 = s[3];
            m2 = s[4];
            if ((h1 == m1 && h2 == m2) 
             || (h1 == h2 && m1 == m2) 
             || (h1 == m2 && h2 == m1))
                cnt++;
        }
        return cnt;    
	}
	
