	public int makeGuestsReturn(int[] a, int[] d)
	{

		boolean[] ok = new boolean[a.length];
		int res = 0;
		while (true) {
			int toCover = Integer.MAX_VALUE;
			for (int i = 0; i < a.length; ++i) if (!ok[i]) {
				toCover = Math.min(toCover, d[i]);
			}
			if (toCover == Integer.MAX_VALUE) break;
			int maxRight = toCover;
			for (int i = 0; i < a.length; ++i)
				if (a[i] <= toCover) maxRight = Math.max(maxRight, d[i]);
			for (int i = 0; i < a.length; ++i)
				if (a[i] <= maxRight) ok[i] = true;
			++res;
		}
		return res;
	}
	
