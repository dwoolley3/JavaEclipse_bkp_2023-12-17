	public int countWays(int[] h)
	{
		HashSet<String> hs = new HashSet<>();
		int n = h.length;
		
		for (int i = 0; i < n; i++) {
			int pos = 0;		
			String s = "";
			int[] h2 = new int[n-1];
			
			for (int j = 0; j < n; j++)
				if (i != j) 
					h2[pos++] = h[j];
			
			Arrays.sort(h2);
			for (int j = 0; j < n; j++)
				if (i != j) 
					s += Arrays.binarySearch(h2, h[j]) + ",";

			hs.add(s);
		}
		return hs.size();
	}
	
