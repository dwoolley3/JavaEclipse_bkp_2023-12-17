	public String verify(int N, String[] openingTime, String[] closingTime)
	{
		int ho, mo, hc, mc;
		int op, cl;
		for (int i = 0; i < 24 * 60; i++) {
			boolean ok = false;
			for (int j = 0; j < N && !ok; j++) {
				ho = Integer.parseInt(openingTime[j].substring(0,2));
				mo = Integer.parseInt(openingTime[j].substring(3,5));
				hc = Integer.parseInt(closingTime[j].substring(0,2));
				mc = Integer.parseInt(closingTime[j].substring(3,5));
				op = ho * 60 + mo;
				cl = hc * 60 + mc;
				if (cl < op) cl += (24 * 60);
				if (op <= i && cl > i) ok = true;
				if (op <= i+24*60 && cl > i+24*60) ok = true;
			}
			if (!ok) return "incorrect";			
		}
		return "correct";
	}
	
