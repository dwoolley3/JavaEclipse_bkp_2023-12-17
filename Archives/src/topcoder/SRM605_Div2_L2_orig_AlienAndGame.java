	public int getNumber(String[] board)
	{
		int n = board.length;
		int width = board[0].length();

		int[][] contig = new int[n][width];
		for (int i = 0; i < n; i++)
			for (int j = 0; j < width; j++)
			{
				char c = board[i].charAt(j);
				boolean contiguous = true;
				for (int k = j; k < width; k++)
					if (c == board[i].charAt(k) && contiguous)
						contig[i][j]++;
					else
						contiguous = false;
			}
		
		int maxLen = 0;
		for (int i = 0; i < n; i++)
			for (int j = 0; j < width; j++)
			{
				int max = 100;
				for (int k = i; k < n; k++)
				{
					max = Math.min(max, contig[k][j]);
					maxLen = Math.max(maxLen, Math.min(max,  (k-i)+1));
				}		
			}
		
		return maxLen*maxLen;		
	}
	
