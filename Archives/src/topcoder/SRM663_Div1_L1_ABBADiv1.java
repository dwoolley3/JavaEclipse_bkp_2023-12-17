	public String canObtain(String initial, String target)
	{
		int iLen = initial.length(), tLen = target.length();
		for (int k = 0; k <= 1; k++)
		{	
			if (k == 1) // reverse initial
			{
				String t = "";
				for (int i = iLen-1; i >= 0; i--)
					t += initial.charAt(i);
				initial = t;
			}
			
			for (int i = 0; i <= tLen - iLen; i++)
			{
				if (target.substring(i,i + iLen).equals(initial))
				{
					if (i > 0 && target.charAt(0) == 'A') continue;
					
					int cntB1 = 0, cntB2 = 0;
					for (int j = 0; j < i; j++)
						if (target.charAt(j) == 'B') cntB1++;
					for (int j = i+ iLen; j < tLen; j++)
						if (target.charAt(j) == 'B') cntB2++;
					
					if (cntB1 - cntB2 == k)
					{
						System.out.println(cntB1 + " " + cntB2);
						return "Possible";
					}
				}
			}
		}
		
		return "Impossible";
	}
	
