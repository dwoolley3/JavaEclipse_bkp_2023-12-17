	{
        int n = A.length;

		g = new ArrayList[N+1];
		for (int i = 0; i <= N; ++i) 
			g[i] = new ArrayList<Integer>();
		
		for (int i = 0; i < n; ++i)
		{
			int u = A[i];
			int v = B[i];
			g[u].add(v);
			g[v].add(u);
		}

		for (int i = 0; i < N; i++)
		{
			depth = 0;		
			used = new boolean[N];
			dfs(i, depth);
			if (maxDepth >= 4) return "Yay!";
		}   	
       
        return ":("; 
	}
	
	private void dfs(int v, int depth) 
	{		
		used[v] = true;
		if (depth >= 4) 
		{
			maxDepth = 4;
			return;
		}
		for (int i = 0; i < g[v].size(); i++)
		{
			int y = g[v].get(i);
			if (!used[y]) 
			{
				dfs(y, depth + 1);
			}
		}
		used[v] = false;  //Left off this line in contest :(
	}
	
