	public String getMessage(String original)
	{
        String v = "aeiou";
        String[] words = original.split(" ");
        String st = "";
        for (String w : words)
        {
            if (!st.isEmpty()) st += " ";
            boolean allVow = true;
            for (int i = 0; i < w.length(); i++)
                if (v.indexOf(w.charAt(i)+"") == -1)  
                    allVow = false;
            if (allVow)
                st += w;
            else //at least 1 consonant
            {
                for (int i = 0; i < w.length(); i++)
                    if (!v.contains(w.charAt(i)+""))
                    {
                        if (i == 0 || v.contains(w.charAt(i-1)+""))
                            st += w.charAt(i);
                    }
            }
        }
        return st;
	}
	
