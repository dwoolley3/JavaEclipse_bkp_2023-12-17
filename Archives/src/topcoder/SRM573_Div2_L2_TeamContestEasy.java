	public int worstRank(int[] s)
	{
        int n = s.length;
        int N = n / 3;
        int st1 = (s[0] + s[1] + s[2]) - Math.min(Math.min(s[0], s[1]), s[2]);
        int rank = 1;
        int[] b = new int[n - 3];
        for (int i = 3; i < n; i++) b[i-3] = s[i];
        Arrays.sort(b);
        //Arrays.Reverse(b);
        for (int i = 0; i < b.length / 2; i++)
        {
        	int t = b[i]; b[i] = b[b.length - 1 - i]; b[b.length - 1 - i] = t;
        }

        int en = (N-1) * 3 - (N-1);

        boolean[] used = new boolean[n - 3];
        for (int i = 0; i < en -1; i++)
            for (int j = en - 1; j > i; j--)
            {
                if (used[j]) continue;
                if (b[i] + b[j] > st1)
                {
                    used[j] = true;
                    used[i] = true;
                    rank++;
                    break;
                }
            }               
        
        return rank;
	}
	
