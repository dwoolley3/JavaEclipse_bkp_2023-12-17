	public int maxLength(String letters)
	{
        int n = letters.length();
        int max = 0;
        for (int i = 0; i < n; i++)
            for (int j = 1; j <= n; j++)
            {
                if (i + j > n) continue;
                String ss = letters.substring(i, i+j);
                if (noRepeats(ss))
                    max = Math.max(max, j);
            }       
        return max;
	}
	
    public boolean noRepeats(String s)
    {
        int[] a = new int[128];
        for (char c : s.toCharArray())
            if (a[c]++ == 0) return false;
        return true;
    }
    
