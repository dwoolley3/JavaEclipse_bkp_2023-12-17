	public int[] solve(int n)
	{
		for (int i = 2; i < n-1; i++)
			if (!isPrime(i) && !isPrime(n-i))
				return new int[]{i,n-i};
		return new int[]{0,0};
	}
	
    boolean isPrime(int n)
    {
        if (n <= 1) return false;
        if (n == 2) return true;
        if (n % 2 == 0) return false;
        for (int i = 3; i * i <= n; i += 2)
            if (n % i == 0) return false;
        return true;
    }
	
