	{
		Arrays.sort(c);
		int n = c.length;
		boolean allEqual = true;
		for (int i = 0; i < n; i++)
			if (c[i] != c[0]) allEqual = false;
		if (allEqual) return 0;
		
		int dist = c[n-1] - c[0];
		double mid = (double)c[0] + dist/2.0;
		
		boolean eqMid = false;
		int max = Integer.MIN_VALUE, min = Integer.MAX_VALUE;
		for (int i = 0; i < n; i++)
		{
			if (c[i] < mid)
			{
				c[i] += X;
				min = Math.min(min, c[i]);
				max = Math.max(max, c[i]);
			}
			else if (c[i] > mid)
			{
				c[i] -= X;
				min = Math.min(min, c[i]);
				max = Math.max(max, c[i]);
			}
			else 
				eqMid = true;
		}
		
		if (eqMid)
		if (Math.abs(max - mid) > Math.abs(mid - min))
			max = Math.max(max, (int)mid + X);
		else
			min = Math.min(min, (int)mid -X);
		
		//Arrays.sort(c);
		int dist2 = max - min; //c[n-1] - c[0];
		return Math.min(dist, dist2);
	}
	
