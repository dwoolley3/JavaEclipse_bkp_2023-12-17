	public int countfee(int[] x, int[] y)
	{
		int n = x.length;
		int[] v = new int[55];
		int ans = 0;
		for (int i = 0; i < n; i++)
		{
			v[x[i]]++; 
			v[y[i]]++;
		}
		for (int i = 0; i < 50; i++)
		{
			int mn =-1, nx=-1;
			for (int j = 0; j < 50; j++)
			{
				if (v[j]==-1) continue;
				if (mn<0||mn>v[j]) {mn=v[j];nx=j;}
			}
			v[nx]=-1;
			for (int j=0; j < n; j++)
			{
				if (x[j]==nx && v[y[j]]>0) v[y[j]]+=50;
				if (y[j]==nx && v[x[j]]>0) v[x[j]]+=50;
			}
			mn=0;
			for (int j=0; j<n; j++)
				if (v[x[j]]==-1 && v[y[j]]==-1) mn++;
			ans +=mn;
		}
		

		return ans;
	}
	
