	public int theMin(String[] board)
	{
        int rows = board.length;
        int cols = board[0].length();
        int r, c, count = 0;

        for (r = 0; r < rows; r++)
        {
            boolean allB = true;
            for (c = 0; c < cols; c++)
                if (board[r].charAt(c) == 'W')
                    allB = false;
            if (allB) count++;
        }

        for (c = 0; c < cols; c++)
        {
            boolean allB = true;
            for (r = 0; r < rows; r++)
                if (board[r].charAt(c) == 'W')
                    allB = false;
            if (allB) count++;
        }
        if (rows + cols == count) return Math.min(rows, cols);
        return count;

	}
	
