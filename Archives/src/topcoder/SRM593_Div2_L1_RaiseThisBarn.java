	public int calc(String str)
	{
        int n = str.length();
        int c = 0;
        for (int i = 0; i < n; i++)
            if (str.charAt(i) == 'c') c++;

        int cnt = 0;
        if (c % 2 == 0)
        {
            for (int i = 0; i < n-1; i++)
            {
                int c2 = 0;
                for (int j = 0; j <= i; j++)
                    if (str.charAt(j) == 'c') c2++;
                if (c2 == c / 2) cnt++;
            }
        }
        return cnt;
	}
	
