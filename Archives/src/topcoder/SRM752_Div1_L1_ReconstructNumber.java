	public String smallest(String comparisons)
	{
		int n = comparisons.length();
		int[] a = new int [n+1];
		a[0] = 1;
		for (int i = 1; i <= n; i++) {
			char c = comparisons.charAt(i-1);
			if (c == '=') 
				a[i] = a[i-1];
			else if (c == '<') {
				a[i] = a[i-1] + 1;
				if (a[i] > 9) return "";
			} else if (c == '>') {
				if (a[i-1] > 0) {
					a[i] = 0;
				} else { // prior digit is 0
					char cprior = ' ';
					for (int j = i-1; j >= 0; j--) {						
						a[j]++;
						if (a[j] > 9) return "";
						if (j > 0) {
							cprior = comparisons.charAt(j-1);
							if (cprior == '>' && a[j-1] > a[j]) break;
							if (cprior == '!' && a[j-1] != a[j]) break;
							if (cprior == '<' && a[j-1] < a[j]) break;
						}
					}
				}
			}
			else if (c == '!') {
				if (a[i-1] == 0)
					a[i] = 1;
				// else a[i] = 0
			}
			
		}
		StringBuilder sb = new StringBuilder();
		for (int i : a) 
			sb.append(i);
		return sb.toString();
	}
	
