	public int mostAttractivePrice(int minPrice, int maxPrice)
	{
        int i, max = Integer.MIN_VALUE, maxi = 0;
        for (i = maxPrice; i >= minPrice; i--)
        {
            String s = i+"";
            int c = 0;
            for (int j = s.length() - 1; j >= 0; j--)
                if (s.charAt(j) != '9') break;
                else c++;
            if (c > max)
            {
                max = c;
                maxi = i;
            }
        }
        return maxi;
	}
	
