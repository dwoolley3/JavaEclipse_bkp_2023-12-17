	{
		String[] s = new String[50];
		
		char[][] c = new char[50][50];
		for (int i = 0; i < 50; i++)
			Arrays.fill(c[i], '.');
				
		if (N % 2 == 1) {
			c[0][0] = 'X';
			c[0][1] = 'X';
			c[0][2] = 'X';
			N--;
		}
		for (int i = 3; i < 50; i +=3) {
			if (N >= 2) {
				c[i][0] = 'X';
				c[i][1] = 'X';
				c[i][2] = 'X';
				N -= 2;
			}
			for (int j = 3; j < 50 && N > 0; j++) {
				c[i][j] = 'X';
				N -= 2;
			}
		}
		for (int i = 0; i < 50; i++) {
			s[i] = new String(c[i]);	
		}
		
		return s;
	}
	
