	public String classifyIt(String word)
	{
        if (isPal(word)) return "PALINDROME";

        String nw = word.charAt(0)+"";
        int n = word.length();
        for (int i = 1; i < n; i++)
            if (word.charAt(i) != word.charAt(i - 1)) nw += word.charAt(i);

        if (isPal(nw)) return "FAUX";
        return "NOT EVEN FAUX";
	}
	
	private boolean isPal(String s)
	{
		int n = s.length();
		for (int i = 0; i < n/2; i++)
			if (s.charAt(i) != s.charAt(n-1-i))	return false;
		return true;
	}
	
