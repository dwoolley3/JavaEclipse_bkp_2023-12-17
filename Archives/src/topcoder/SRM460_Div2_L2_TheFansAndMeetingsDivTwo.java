	public double find(int[] minJ, int[] maxJ, int[] minB, int[] maxB)
	{
        int nJ = minJ.length;
        int nB = minB.length;
        int[] J = new int[51];
        for (int i = 0; i < nJ; i++)
            for (int j = minJ[i]; j <= maxJ[i]; j++)
                J[j]++;

        int[] B = new int[51];
        for (int i = 0; i < nB; i++)
            for (int j = minB[i]; j <= maxB[i]; j++)
                B[j]++;


        double[] pJ = new double[51];
        double[] pB = new double[51];
        double p = 0;
        for (int nf = 1; nf <= 50; nf++)
        {
            if (J[nf] > 0 && B[nf] > 0)
            {
                for (int i = 0; i < nJ; i++)
                    if (nf >= minJ[i] && nf <= maxJ[i])
                        pJ[nf] += (1.0 / (maxJ[i] - minJ[i] + 1)) * (1.0 / nJ);

                for (int i = 0; i < nB; i++)
                    if (nf >= minB[i] && nf <= maxB[i])
                        pB[nf] += (1.0 / (maxB[i] - minB[i] + 1)) * (1.0 / nB);

                p += pJ[nf] * pB[nf];
            }
        }
        return p;
	}
	
