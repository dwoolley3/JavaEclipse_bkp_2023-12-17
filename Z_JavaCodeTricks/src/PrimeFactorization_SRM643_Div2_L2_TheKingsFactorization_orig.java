	public long[] getVector(long N, long[] primes)
	{
		for (long x : primes)
			N /= x;
		
		int numP = primes.length;
		int maxNum = numP * 2;
		
		ArrayList<Long> al = new ArrayList<Long>();
		while (N % 2L == 0)
		{
			al.add(2L);
			N /= 2;
			numP++;
		}
		
		//Max for i will be < 1_000_000 except possibly the last item.
		//We could also take N as soon as i exceeds last of primes[].
		//Below, we immediately take N when only 1 prime remains to be found.
		//i could start at 2 and be incremented by 1. Performance is fine here.
        for (long i = 3; i * i <= N && numP < maxNum-1; i+=2)
	        while (N % i == 0)
	        {
				al.add(i);
				N /= i;
				numP++;
	        }
        if (N > 1)
        	al.add(N);
        for (long x : primes)
        	al.add(x);
        Collections.sort(al);
        int n = al.size();
        long[] ans = new long[n];
        for (int i = 0; i < n; i++)
        	ans[i] = al.get(i);
        return ans;
	
	}
	
