	public int timeToWin(String[] states)
	{
		String st = "";
		int n = states.length;
		int len = states[0].length();
		for (int i = 0; i < len; i++) st += "-";
		
		int cnt = 0;
		for (int row = 0; row < n; row++)
		{
			boolean definitePlus = false, definiteMin = false;
			for (int i = 0; i < len; i++)
			{
				char c = states[row].charAt(i);			
				if (st.charAt(i) == '-' && c == '+')
					definitePlus = true;
				else if (st.charAt(i) == '+' && c == '-')
					definiteMin = true;
			}
			String st2 = "";
			for (int i = 0; i < len; i++)
			{
				char c = states[row].charAt(i);			
				if (c == '?')			
				{
					int r = row+1;
					while (r < n && states[r].charAt(i) == '?')
						r++;
					c = st.charAt(i);
					if (r < n) {
						if (st.charAt(i) == '-' && states[r].charAt(i) == '+')
							if (definitePlus)
								c = '+';
						if (st.charAt(i) == '+' && states[r].charAt(i) == '-')
							if (definiteMin)
								c = '-';
					}
				}
				st2 += c;
			}
			st = st2;
			if (definitePlus) cnt++;
			if (definiteMin) cnt++;
		}
		
		return cnt + n;
	}
	
