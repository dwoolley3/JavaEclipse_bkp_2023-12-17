	public String getMin(String S, int L)
	{
		ArrayList<String> a = new ArrayList<String>();
		int n = S.length();
		for (int i = 0; i < n-L+1; i++)
		{
			char[] c = new char[L];			
			for (int j = 0; j < L; j++)
				c[j] = S.charAt(i+j);
			Arrays.sort(c);
			String st = new String();
			for (int j = 0; j < i; j++)
				st += S.charAt(j);
			for (int j = 0; j < L; j++)
				st += c[j];
			for (int j = i+L; j < n; j++)
				st += S.charAt(j);
			a.add(st);
		}
		Collections.sort(a);
		return a.get(0);
	}
	
