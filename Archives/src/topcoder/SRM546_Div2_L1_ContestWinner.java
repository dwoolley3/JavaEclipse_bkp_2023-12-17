	public int getWinner(int[] events)
	{
        int[] cont = new int[50];
        int max = 0, contestant = events[0];

        for (int i = 0; i < events.length; i++)
        {
             for (int j = 0; j < i; j++)
             {
                 if (events[i] == events[j])
                 {
                     cont[i]++;
                     if (cont[i] > max)
                     {
                         max = cont[i];
                         contestant = events[i];
                     }
                 }
             }
        }
        
        return contestant;
	}
	
