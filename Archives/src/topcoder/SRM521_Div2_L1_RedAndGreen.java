	public int minPaints(String row)
	{
        int n = row.length();
        int min = n;
        for (int i = 0; i <= n; i++)
        {
            int count = 0;
            for (int j = 0; j < n; j++)
            {
                if (j < i && row.charAt(j) == 'G') count++;
                if (j >= i && row.charAt(j) == 'R') count++;             
            }
            if (count < min) min = count;
        }
        return min;
	}
	
