	public int find(int[] A, int[] B)
	{
        int na = A.length;
        int nb = B.length;
        int[] a = new int[100000];
        for (int i = 0; i < na; i++)
            a[i] = A[i];
        for (int i = na; i < 100000; i++)
            for (int j = 1; j <= na; j++)
                a[i] = (a[i] + a[i - j]) % 10;

        for (int i = 0; i < 100000 - nb; i++)
        {
            int j;
            for (j = 0; j < nb; j++)
                if (a[i+j] != B[j]) break;
            if (j == nb) return i;
        }                

        return -1;
	}
	
