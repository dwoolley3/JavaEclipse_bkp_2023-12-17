	public int count(String s, int k)
	{
		int[] a = new int[26];
		for (int i = 0; i < s.length(); i++)
		{
			char c = s.charAt(i);
			a[c-'a']++;
		}
		
		while (k > 0)
		{
			int max = 0, maxi = 0;
			for (int i = 0; i < 26; i++)
				if (a[i] > max)
				{
					max = a[i];
					maxi = i;
				}
			a[maxi]--;
			k--;
		}
		int ans = 0;
		for (int i = 0; i < 26; i++)
			ans += a[i]*a[i];
		return ans;		
	}
	
