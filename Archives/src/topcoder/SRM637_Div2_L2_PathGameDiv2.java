	public int calc(String[] b)
	{
		int n = b[0].length(), cnt = 0;
		int pos = -1; //0 = top, 1 = bottom
		for (int i = 0; i < n; i++)
		{
			if (b[0].charAt(i) == '#') pos = 0;
			else if (b[1].charAt(i) == '#') pos = 1;
			else if (pos == -1) cnt++;
			else if (i == n-1
				|| (i < n-1 && b[0].charAt(i+1) == '.' && b[1].charAt(i+1) == '.')
				|| (i < n-1 && b[pos].charAt(i+1) == '#'))
				cnt++;			
		}
		return cnt;
	}
	
