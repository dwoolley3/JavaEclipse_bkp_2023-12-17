	public int[] evaluateRoll(int T, int[] pd, int[] nd)
	{
		int numnd = nd.length;
		int[] pos = new int[7];
		int[] neg = new int[7];
		for (int i: pd) {
			pos[i]++;
		}
		for (int i: nd) {
			neg[i]++;
		}
		for (int i = 1; i <=6; i++) {
			int take = Math.min(pos[i], neg[i]);
			pos[i] -= take;
			neg[i] -= take;
			numnd -= take;
		}
		
		int x=0 ,y= numnd;
		for (int i = 1; i <= T && x == 0; i++) {
			if (pos[i] > 0) {
				x = 1;
			}
		}		
		
		return new int[] {x,y};
	}
	
