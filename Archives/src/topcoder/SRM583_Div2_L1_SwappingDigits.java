	public String minNumber(String num)
	{
		int n = num.length();
		String ans = num;
		char[] c = num.toCharArray();
		for (int i = 0; i < n; i++)
			for (int j = 0; j < n; j++)				
			{
				char t = c[i]; c[i] = c[j]; c[j] = t;
				if (c[0] != '0')
				{
					String s = new String(c);
					if (s.compareTo(ans) < 0)
						ans = s;
				}				
				t = c[i]; c[i] = c[j]; c[j] = t;
			}

		return ans;
	}
	
