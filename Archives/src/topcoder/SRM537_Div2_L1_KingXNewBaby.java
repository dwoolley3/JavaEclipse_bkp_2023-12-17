	public String isValid(String name)
	{
        boolean isValid = true;
        char ch;
        int vowNum = 0;
        char vow = ' ';
        if (name.length() != 8)
            isValid = false;
        else {
            for (int i = 0; i < name.length(); i++)
            {
                ch = name.charAt(i);
                if (ch < 'a' || ch > 'z')
                    isValid = false;
                else
                {
                    if ("aeiou".toString().indexOf(ch) > -1)
                    {
                        vowNum++;
                        if (vowNum > 2)
                            isValid = false;
                        else if (vowNum == 2 && vow != ch)
                            isValid = false;
                        else
                            vow = ch;
                    }
                }
                if (!isValid) break;
            }  
            if (vowNum != 2) isValid = false;
        }
        return (isValid) ? "YES" : "NO";
	}
	
