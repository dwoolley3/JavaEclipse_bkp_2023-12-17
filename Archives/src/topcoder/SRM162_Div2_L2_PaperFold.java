	public int numFolds(int[] paper, int[] box)
	{
        int num = 10;
        for (int i = 0; i < 9; i++)
            for (int j = 0; j < 9 - i; j++)
            {
                double pW = paper[0];
                double pL = paper[1];
                double bW = box[0];
                double bL = box[1];

                pW = pW / (double)(1 << i);  // 2^i
                pL = pL / (double)(1 << j);  // 2^j

                if ((pW <= bW && pL <= bL) || (pW <= bL && pL <= bW))
                    num = Math.min(num, i + j);
            }
        return (num <= 8) ? num : -1;
	}
	
