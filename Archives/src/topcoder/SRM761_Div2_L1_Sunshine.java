	public String weatherReport(int totalDays, int rainyDays)
	{
		int r = rainyDays;
		int s = totalDays - rainyDays;
		int max = totalDays / (rainyDays + 1);
		System.out.println(max);
		StringBuilder sb = new StringBuilder();
		while (s > 0) {
			int min = Math.min(s, max);
			for (int i = 0; i < min; i++) {
				sb.append("S");
			}
			s -= min;
			if (r > 0) {
				sb.append("R");
				r--;
			}
		}
		for (int i = 0; i < r; i++) {
			sb.append("R");
		}

		return sb.toString();
	}
	
