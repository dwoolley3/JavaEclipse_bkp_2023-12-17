	public String equal(String s, String t)
	{
		int slen = s.length();
		int tlen = t.length();
		long lcm = LCM(slen, tlen);
		
		String ns = "", nt = "";
		for (int i = 0; i < lcm / slen; i++)
			ns += s;
		for (int i = 0; i < lcm / tlen; i++)
			nt += t;
		
		if (ns.equals(nt))
			return "Equal";
		return "Not equal";
					
	}
	
    long GCD(long a, long b)
    {
        if (b == 0) return a;
        return GCD(b, a % b);
    }
    long LCM(long a, long b)
    {
        return a * b / GCD(a, b);
    }

	
